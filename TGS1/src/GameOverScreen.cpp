#include "GameOverScreen.h"


//----------------------------------------------------------------------
// コンストラクタ
//----------------------------------------------------------------------
GameOverScreen::GameOverScreen()
{
	time = 0.0f;
	death = false;
	count = 0;
}
//----------------------------------------------------------------------
// デストラクタ
//----------------------------------------------------------------------
GameOverScreen::~GameOverScreen()
{
}
//----------------------------------------------------------------------
// 初期化処理
//----------------------------------------------------------------------
void GameOverScreen::Init()
{
}
//----------------------------------------------------------------------
// 更新処理
//----------------------------------------------------------------------
void GameOverScreen::Update()
{
	time += 0.1f;
	if (time > 15.0f) {
		death = true;
	}
	if (death == true) {
		count++;
	}
}
//----------------------------------------------------------------------
// 描画処理
//----------------------------------------------------------------------
void GameOverScreen::Draw(Font font, Font font1)
{
	font1(L"GAME OVER").draw(WINDOW_W / 2 - 350, WINDOW_H / 2 - 200,Palette::Red);

	if (death == true) {
		if (count > 60) {
			font(L"PUSH A").draw(WINDOW_W / 2 - 140, WINDOW_H / 2 + 100,Palette::Yellow);
		}
		if (count > 120) {
			count = 0;
		}
	}

}
//----------------------------------------------------------------------
// 終了処理
//----------------------------------------------------------------------
void GameOverScreen::Exit()
{
}
