#include "StartScreen.h"


//----------------------------------------------------------------------
// コンストラクタ
//----------------------------------------------------------------------
StartScreen::StartScreen()
{
	start = false;
	time = 0;
}
//----------------------------------------------------------------------
// デストラクタ
//----------------------------------------------------------------------
StartScreen::~StartScreen()
{
}
//----------------------------------------------------------------------
// 初期化処理
//----------------------------------------------------------------------
void StartScreen::Init()
{
}
//----------------------------------------------------------------------
// 更新処理
//----------------------------------------------------------------------
void StartScreen::Update()
{
	time++;
	if (Input::KeySpace.clicked) {
		start = true;
	}
	if (controller.buttonA.clicked) {
		start = true;
	}
}
//----------------------------------------------------------------------
// 描画処理
//----------------------------------------------------------------------
void StartScreen::Draw(Font font, Font font1)
{
	font1(L" Trap ").draw(WINDOW_W / 2 - 200, WINDOW_H / 2 - 300,Palette::Red);

	
	if (time > 60) {
		font(L"PUSH A").draw(WINDOW_W / 2 - 140, WINDOW_H / 2 + 100);
	}
	if (time == 120) {
		time = 0;
	}
}
//----------------------------------------------------------------------
// 終了処理
//----------------------------------------------------------------------
void StartScreen::Exit()
{
}
